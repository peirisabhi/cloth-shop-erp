    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        
        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);
        
        ResultSet rs = search("SELECT * FROM product INNER JOIN brand INNER JOIN stock WHERE product.brand_id = brand.id AND product.id = stock.product_id  AND product.`status`='1' && product.`desc` LIKE '"+jTextField1.getText()+"%'");
        
         int i =1;
        try {
            while (rs.next()) {
                Vector<String> v = new Vector<>();

                v.add(rs.getString("id"));
                v.add(rs.getString("barcode"));
                v.add(rs.getString("name"));
                v.add(rs.getString("desc"));
                v.add(rs.getString("unit"));
                v.add(rs.getString("u_price"));
                v.add(rs.getString("discount"));
                v.add(rs.getString("price"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("critical"));

                dtm.addRow(v);
                
                jLabel18.setText(String.valueOf(i));
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTextField1KeyReleased

