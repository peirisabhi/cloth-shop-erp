     void tableLoad() {

        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = search("SELECT * FROM product INNER JOIN brand INNER JOIN stock WHERE product.brand_id = brand.id AND product.id = stock.product_id  AND product.`status`='1'");
            int i =1;
            while (rs.next()) {
                Vector<String> v = new Vector<>();

                v.add(rs.getString("id"));
                v.add(rs.getString("barcode"));
                v.add(rs.getString("name"));
                v.add(rs.getString("desc"));
                v.add(rs.getString("unit"));
                v.add(rs.getString("u_price"));
                v.add(rs.getString("discount"));
                v.add(rs.getString("price"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("critical"));

                dtm.addRow(v);
                
                jLabel18.setText(String.valueOf(i));
                i++;
            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }

     
