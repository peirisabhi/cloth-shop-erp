    void tableLoad() {

        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = search("SELECT * FROM product INNER JOIN stock WHERE product.id = stock.product_id  AND product.`status`='1'");
            int i = 1;
            while (rs.next()) {
                Vector<Object> v = new Vector<Object>();

                v.add(i);
                v.add(rs.getString("desc"));
                v.add(rs.getString("discount"));
                v.add(rs.getString("u_price"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("price"));
                i++;
                dtm.addRow(v);
            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }
    
    
