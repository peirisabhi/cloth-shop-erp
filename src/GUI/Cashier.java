    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        if (!jTextField1.getText().equals("")) {
            ResultSet rs = search("SELECT * FROM product INNER JOIN stock INNER JOIN brand WHERE product.id = stock.product_id  && product.`status`='1' && product.brand_id = brand.id && product.barcode = '" + jTextField1.getText() + "'");

            try {
                if (rs.next()) {
                    HashMap<String, String> prod = new HashMap<String, String>();
                    prod.put("productId", rs.getString("id"));
                    prod.put("barcode", rs.getString("barcode"));
                    prod.put("desc", rs.getString("desc"));
                    prod.put("discount", rs.getString("discount"));
                    prod.put("brand", rs.getString("brand.name"));
                    prod.put("uPrice", rs.getString("u_price"));
                    prod.put("unit", rs.getString("unit"));
                    prod.put("price", rs.getString("price"));
                    prod.put("qty", rs.getString("qty"));

                    new productDetails(prod, this).setVisible(true);

                }
            } catch (SQLException ex) {
                Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jTextField1KeyReleased

