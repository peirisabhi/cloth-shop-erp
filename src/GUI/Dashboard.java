    void details() {
        ResultSet rs1 = search("SELECT COUNT(id)  AS count FROM product WHERE product.`status` = '1'");
    
        ResultSet rs2 = search("SELECT SUM(invoice.total) AS sales FROM `transaction` INNER JOIN invoice WHERE invoice.id = `transaction`.invoice_id && DATE(invoice.date) = CURDATE()");
        
        ResultSet rs3 = search("SELECT SUM(qty) AS stock FROM stock WHERE stock.`status` = '1'");
        
        ResultSet rs4 = search("SELECT COUNT(id) AS critical FROM stock WHERE stock.qty < stock.critical;");
        
        try {
            if(rs1.next()){
                jLabel34.setText(rs1.getString("count").toString());
            }
            if(rs2.next()){
                jLabel38.setText("Rs."+rs2.getString("sales").toString());
            }
            if(rs3.next()){
                jLabel46.setText(rs3.getString("stock").toString());
            }
            if(rs4.next()){
                jLabel42.setText(rs4.getString("critical").toString());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
