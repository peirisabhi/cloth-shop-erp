    void tableLoad() {

        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {

            ResultSet rs = search("SELECT * FROM user_logs INNER JOIN employee INNER JOIN position WHERE user_logs.employee_id = employee.id && employee.position_id = position.id");
            int i = 1;
            while (rs.next()) {
                Vector<Object> v = new Vector<>();

                v.add(i);
                v.add(rs.getString("employee.id"));
                v.add(rs.getString("employee.name"));
                v.add(rs.getString("position.position"));
                v.add(rs.getString("log"));
                v.add(rs.getString("log_out"));

                dtm.addRow(v);
                i++;

            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }

    

