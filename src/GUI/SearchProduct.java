    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == 10) {

            int row = jTable1.getSelectedRow();

            if (row != -1) {
                HashMap<String, String> prod = new HashMap<String, String>();
                prod.put("productId", jTable1.getValueAt(row, 0).toString());
                prod.put("barcode", jTable1.getValueAt(row, 1).toString());
                prod.put("desc", jTable1.getValueAt(row, 3).toString());
                prod.put("discount", jTable1.getValueAt(row, 6).toString());
                prod.put("brand", jTable1.getValueAt(row, 2).toString());
                prod.put("uPrice", jTable1.getValueAt(row, 5).toString());
                prod.put("unit", jTable1.getValueAt(row, 4).toString());
                prod.put("price", jTable1.getValueAt(row, 7).toString());
                prod.put("qty", jTable1.getValueAt(row, 8).toString());

                new productDetails(prod, this.c).setVisible(true);
                this.dispose();
            }

        } else if (evt.getKeyCode() == 27) {
            this.dispose();
        }
    }//GEN-LAST:event_jTable1KeyPressed

