    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = search("SELECT * FROM product INNER JOIN stock INNER JOIN brand WHERE product.id = stock.product_id  AND product.brand_id = brand.id  AND product.`status`='1' && product.desc LIKE '"+jTextField1.getText()+"%'");
            int i = 1;
            while (rs.next()) {
                Vector<Object> v = new Vector<Object>();

                v.add(rs.getString("product.id"));
                v.add(rs.getString("product.barcode"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("product.desc"));
                v.add(rs.getString("unit"));
                v.add(rs.getString("u_price"));
                v.add(rs.getString("discount"));
                v.add(rs.getString("price"));
                v.add(rs.getString("qty"));
                i++;
                dtm.addRow(v);
            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_jTextField1KeyReleased

