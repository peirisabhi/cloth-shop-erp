    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        
         TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = search("SELECT * FROM employee INNER JOIN position WHERE employee.position_id = position.id && STATUS = '1' && employee.name LIKE '"+jTextField1.getText()+"%'");
            int i = 1;
            while (rs.next()) {
                Vector<Object> v = new Vector<>();

                v.add(i);
                v.add(rs.getString("name"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("email"));
                v.add(rs.getString("position"));

                dtm.addRow(v);
                jLabel16.setText(String.valueOf(i));
                i++;
            }

        } catch (Exception e) {
            System.out.println(e);
        }

        
    }//GEN-LAST:event_jTextField1KeyReleased

