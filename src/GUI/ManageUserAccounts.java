    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked

        int row = jTable1.getSelectedRow();

        if (row != -1) {
            
            HashMap<String, String> emp = new HashMap<String, String>();
            emp.put("name", jTable1.getValueAt(row, 1).toString());
            emp.put("contact", jTable1.getValueAt(row, 2).toString());
            emp.put("email", jTable1.getValueAt(row, 3).toString());
            emp.put("position", jTable1.getValueAt(row, 3).toString());
            

            ManageUser mu = new ManageUser(emp, this);
            mu.jButton1.setEnabled(false);
            mu.setVisible(true);

        }else {
            JOptionPane.showMessageDialog(this, "Please select Employee", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jLabel5MouseClicked

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        
        int row = jTable1.getSelectedRow();

        if (row != -1) {
            int r = JOptionPane.showConfirmDialog(this, "Are you sure do you want to delete this employee", "Conform", JOptionPane.INFORMATION_MESSAGE);
            if (r == 0) {
                if (1 == iud("UPDATE `cloth_shop`.`employee` SET `status`='0' WHERE  `email`='" + jTable1.getValueAt(row, 3).toString() + "';")) {
                    tableLoad();
                    JOptionPane.showMessageDialog(this, "Deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select employee", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jLabel2MouseClicked

