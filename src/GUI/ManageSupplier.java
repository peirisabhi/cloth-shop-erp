    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked

        int row = jTable1.getSelectedRow();

        if (row != -1) {

            HashMap<String, String> supplier = new HashMap<String, String>();
            supplier.put("supplierId", jTable1.getValueAt(row, 0).toString());
            supplier.put("company", jTable1.getValueAt(row, 1).toString());
            supplier.put("address", jTable1.getValueAt(row, 2).toString());
            supplier.put("contactPerson", jTable1.getValueAt(row, 3).toString());
            supplier.put("mobile", jTable1.getValueAt(row, 4).toString());
            supplier.put("tel", jTable1.getValueAt(row, 5).toString());

            AddUpdateSupplier ads = new AddUpdateSupplier(this, supplier);
            ads.jButton1.setEnabled(false);
            ads.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Please select Supplier", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jLabel5MouseClicked

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        int row = jTable1.getSelectedRow();

        if (row != -1) {
            int r = JOptionPane.showConfirmDialog(this, "Are you sure do you want to delete this Supplier", "Conform", JOptionPane.INFORMATION_MESSAGE);
            if (r == 0) {
                if (1 == iud("UPDATE `cloth_shop`.`supplier` SET `status`='0' WHERE  `id`='" + jTable1.getValueAt(row, 0).toString() + "';")) {
                    tableLoad();
                    JOptionPane.showMessageDialog(this, "Deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select product", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jLabel2MouseClicked

