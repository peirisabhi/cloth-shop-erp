    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
       String username = jTextField2.getText();
       String password = jPasswordField1.getText();
       
        System.out.println(username);
        System.out.println(password);
       
        try {
            ResultSet rs = search("SELECT * FROM employee INNER JOIN position WHERE position.id = employee.position_id && uname = '"+username+"' && pw = '"+password+"' && status =1");
                System.out.println("okk");
            if(rs.next()){
                System.out.println(username.equals(rs.getString("uname")));
                System.out.println(password.equals(rs.getString("pw")));
                if(username.equals(rs.getString("uname")) && password.equals(rs.getString("pw"))){
                    
                    iud("INSERT INTO `cloth_shop`.`user_logs` (`log`, `employee_id`) VALUES (NOW(), '"+rs.getString("id")+"');");
                    ResultSet log = search("SELECT  MAX(id) as logId FROM user_logs;");
                    String logId = "";
                    
                    if(log.next()){
                        logId = log.getString("logId");
                    }
                    
                    
                    if(rs.getString("position").equals("Cashier")  || rs.getString("position").equals("Admin")){
                       
                       Cashier c = new Cashier(rs.getString("name").toString(), Integer.parseInt(rs.getString("id").toString()),logId);
                       c.setVisible(true);
                        this.dispose();
                       
                    }else if (rs.getString("position").equals("Super Admin")) {
                     Menu m = new Menu(rs.getString("name").toString(), Integer.parseInt(rs.getString("id").toString()),logId);
                       m.setVisible(true);
                        this.dispose();

                    }
                    
                }else{
                     JOptionPane.showMessageDialog(this, "Incorrect Authentication", "Login Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
            
                JOptionPane.showMessageDialog(this, "Incorrect Authentication", "Login Error", JOptionPane.ERROR_MESSAGE);
            
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

