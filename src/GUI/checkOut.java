    private void jTextField8KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyReleased

        if (!jTextField8.getText().equals("")) {
            if (Double.parseDouble(jTextField8.getText()) >= Double.parseDouble(jLabel14.getText())) {
                jLabel12.setText(String.valueOf(Double.parseDouble(jTextField8.getText()) - Double.parseDouble(jLabel14.getText())));
            } else {
                jLabel12.setText("");
            }
        }

        if (evt.getKeyCode() == 10) {
            if (!jTextField8.getText().equals("")) {
                if (Double.parseDouble(jTextField8.getText()) >= Double.parseDouble(jLabel14.getText())) {

                    int r = JOptionPane.showConfirmDialog(this, "Are you sure do you want to print receipt", "Conform", JOptionPane.INFORMATION_MESSAGE);

                    if (r == 0) {
//                        print receipt
                        printR();
                    } else if (r == 1) {
                        cancel();
                    }

                } else {

                    JOptionPane.showMessageDialog(this, "Insufficient cash. Unable to proceed", "Warning", JOptionPane.WARNING_MESSAGE);

                }
            }

        } else if (evt.getKeyCode() == 27) {
            this.dispose();
        }
    }//GEN-LAST:event_jTextField8KeyReleased

