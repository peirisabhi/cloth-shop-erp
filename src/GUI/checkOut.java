    synchronized void printR() {

        if (1 == iud("INSERT INTO `cloth_shop`.`invoice` (`total`, `date`, `employee_id`)"
                + " VALUES ('" + jLabel14.getText() + "', NOW(), '" + this.uId + "');")) {

            int rows = this.t.getRowCount();
            ResultSet rs1 = search("SELECT  MAX(id) as invoiceId FROM invoice;");

            try {
                if (rs1.next()) {
                    for (int x = 0; x < rows; x++) {
                        iud("INSERT INTO `cloth_shop`.`invoice_product` (`qty`, `invoice_id`, `product_id`)"
                                + " VALUES ('" + this.t.getValueAt(x, 4) + "', '" + rs1.getString("invoiceId") + "', '" + this.t.getValueAt(x, 0) + "');");

                        iud("UPDATE `cloth_shop`.`stock` SET qty = qty-" + Integer.parseInt(this.t.getValueAt(x, 4).toString()) + " WHERE  `product_id`='" + this.t.getValueAt(x, 0) + "';");

                      
                        
                    }

                    iud("INSERT INTO `cloth_shop`.`transaction` (`amount`, `date`, `employee_id`, `invoice_id`, `status`)"
                            + " VALUES ('" + Double.parseDouble(jTextField8.getText()) + "', NOW(), '" + this.uId + "', '" + rs1.getString("invoiceId") + "', 'Paid');");
                    
                      try {
                        String path = "src//reports//nInvoice.jasper";
                        
                        HashMap<String, Object> map = new HashMap<>();
                        map.put("Parameter1",rs1.getString("invoiceId"));
                        map.put("Parameter2", this.Uname);
                        map.put("Parameter3", jLabel14.getText());
                        map.put("Parameter4", jTextField8.getText());
                        map.put("Parameter5", "0.00");
                        
                        TableModel model = this.t.getModel();
                        
                        JRTableModelDataSource tabelDataSource = new JRTableModelDataSource(model);
                        
                            JasperPrint print = JasperFillManager.fillReport(path, map, tabelDataSource);
                            
                            JasperViewer.viewReport(print,false);
                            
                        } catch (JRException ex) {
                            Logger.getLogger(checkOut.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    
                    
                    clear();
                }
            } catch (SQLException ex) {
                Logger.getLogger(checkOut.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }

