    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("") && !jTextField4.getText().equals("")) {

            int r = JOptionPane.showConfirmDialog(this, "Are you sure do you want to register this employee", "Conform", JOptionPane.INFORMATION_MESSAGE);

            if (r == 0) {

                ResultSet rs = search("SELECT * FROM position WHERE position.position = '" + jComboBox1.getSelectedItem().toString() + "';");

                try {
                    if (rs.next()) {
                        String password = getPassword();
                        if (1 == iud("INSERT INTO `cloth_shop`.`employee` (`name`, `contact`, `email`, `uname`, `pw`, `position_id`, `status`) "
                                + "VALUES ('" + jTextField1.getText() + "', '" + jTextField2.getText() + "', '" + jTextField3.getText() + "', '" + jTextField4.getText() + "', '" + password + "', '" + rs.getString("id") + "', '1');")) {
                            this.mua.tableLoad();
                            sendMail(jTextField3.getText(), password, jTextField1.getText(), jTextField4.getText());
                            JOptionPane.showMessageDialog(this, "Record has been successfully saved,\nPassword sent to " + jTextField3.getText() + "", "Success", JOptionPane.INFORMATION_MESSAGE);
                            clear();
                        } else {
                            JOptionPane.showMessageDialog(this, "Can not complete this process", "Error", JOptionPane.ERROR_MESSAGE);
                        }

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Required field missing", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

